
public class Lorca {
	public void poema() {
		System.out.println("Noche arriba los dos con luna llena,");
		System.out.println("yo me puse a llorar y tú reías.");
		System.out.println("Tu desdén era un dios, las quejas mías");
		System.out.println("momentos y palomas en cadena.");
		System.out.println();
		System.out.println();
		System.out.println("Noche abajo los dos. Cristal de pena,");
		System.out.println("llorabas tú por hondas lejanías.");
		System.out.println("Mi dolor era un grupo de agonías");
		System.out.println("sobre tu débil corazón de arena.");
		System.out.println();
		System.out.println();
		System.out.println("La aurora nos unió sobre la cama,");
		System.out.println("las bocas puestas sobre el chorro helado");
		System.out.println("de una sangre sin fin que se derrama.");
		System.out.println();
		System.out.println();
		System.out.println("Y el sol entró por el balcón cerrado");
		System.out.println("y el coral de la vida abrió su rama");
		System.out.println("sobre mi corazón amortajado.");
	}
}
